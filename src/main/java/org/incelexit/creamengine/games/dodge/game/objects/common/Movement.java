package org.incelexit.creamengine.games.dodge.game.objects.common;

public class Movement {
    private int x;
    private int y;

    public Movement(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getY() {
        return y;
    }

    public int getX() {
        return x;
    }
}
