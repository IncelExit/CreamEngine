package org.incelexit.creamengine.games.dodge.game.objects.common;

public class Extent {
    private final int x;
    private final int y;

    public Extent(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getY() {
        return y;
    }

    public int getX() {
        return x;
    }
}
